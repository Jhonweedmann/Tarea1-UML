public class Articulo {
    private float peso;
    private String nombre;
    private String descrion;
    private float precio = 100;

    public float getPeso() {
        return peso;
    }

    public String getNombre() {
        return nombre;
    }

    public String getDescrion() {
        return descrion;
    }

    public float getPrecio() {
        return precio;
    }
}
